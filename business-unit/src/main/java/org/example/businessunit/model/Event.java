package org.example.businessunit.model;

public class Event {
    private String ts;
    private String ss;
    private String id;
    private String name;
    private String date;
    private String venue;
    private String capturedAt;

    public String getTs() {
        return ts;
    }

    public String getSs() {
        return ss;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public String getVenue() {
        return venue;
    }

    public String getCapturedAt() {
        return capturedAt;
    }
}
